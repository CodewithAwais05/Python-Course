# classes and objects

class Student:
    name = "Awais Raza"  # name of every object is 'Awais Raza'

s1 = Student()  # creating object of Student class
print(s1.name)
s2 = Student()
print(s2.name)

class Car:
    color = "blue"
    brand = "bmw"

c1 = Car()
print(c1.color)
print(c1.brand)