# Program to demonstrate list membership and counting

students = ["Ali", "Sara", "Ali", "Ahmed", "Sara"]

print("Students list:", students)
print("Ali appears", students.count("Ali"), "times")
print("Sara appears", students.count("Sara"), "times")

print("Is Ahmed in the list?", "Ahmed" in students)
print("Is Zain in the list?", "Zain" in students)
